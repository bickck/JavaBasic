

/*
 * 비교 연산자
 * - < , > , <= , >=
 *   == : 같을때가 참
 *   != : 서로 다를때가 참
 *   > 조건식에 따른 결과는 true, false 입니다
 * 
 */

public class Ex03 {

	public static void main(String[] args) {
		
		int ie = 10, ig = 7;
		System.out.println("ie : " + ie + " - ig : " + ig);
		System.out.println();
		
		System.out.println("ie >= ig : " + (ie >= ig));
		//System.out.println("ie >= ig : " + (ie => ig)); Error
		System.out.println("ie == ig : " + (ie == ig));
		System.out.println("ie != ig : " + (ie != ig));
		
		
	}
	
}














