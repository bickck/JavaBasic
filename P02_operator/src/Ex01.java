

/*
 * 산술연산자
 * - + , - , *(곱셈) , /(나눗셈)
 *   % : 나머지 값을 구하는 연산자
 *       > 홀짝수, 배수...
 * 
 */

public class Ex01 {

	public static void main(String[] args) {
		
		int ia = 10, ib = 4;
		System.out.println("ia : " + ia + " - ib : " + ib);
		System.out.println();
		
		System.out.println(ia + " + " + ib + " = " + (ia + ib));
		System.out.println(ia + " - " + ib + " = " + (ia - ib));
		System.out.println(ia + " * " + ib + " = " + (ia * ib));
		System.out.println(ia + " / " + ib + " = " + (ia / ib));
		System.out.println(ia + " % " + ib + " = " + (ia % ib));
		
	}
	
}
















