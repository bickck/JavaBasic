
package day1;


public class Ex05Floating {

	public static void main(String[] args) {
		
		// 실수
		double dd = 1.2;
		System.out.println("dd : " + dd);
		
		float ff = 1.2F;
		// float 타입 리터럴을 사용할 때에는 데이터 뒤에 'F(f)'를 사용합니다
		System.out.println("ff : " + ff);
		
		System.out.println("dd : " + dd + " - ff : " + ff);
		
		
		
	}
	
}





















