
package day1;


public class Ex02Print {

	// main 메서드 자동 완성
	// - main -> Ctrl + space -> Enter
	public static void main(String[] args) {
		
		// println() : 출력 후 라인 변경합니다
		// - sysout -> Ctrl + space
		System.out.println("끈기를 가지고 열심히 하세요...");
		System.out.println("println() 은 출력후 라인 변경 됩니다..");
		
		System.out.println();
		
		// print() : 출력 후 라인 변경되지 않습니다
		System.out.print("print() 는 라인 변경되지 않아요..");
		System.out.println("확인 되시나요??");
		
		System.out.println();
		
		System.out.println(12 + 34);
		
		// 문자열 : 문자들의 조합. " "로 작성합니다
		// 문자열을 더하면 이어지게 됩니다
		System.out.println("abc" + "def");
		System.out.println();
		
		System.out.println(1 + 2 + "data");
		System.out.println("data" + 1 + 2);
		// 문자열 이후에 데이터는 문자열로 인식하게 됩니다
		
		System.out.println("data" + (1 + 2));
		
		
		
		
	}
	
}























