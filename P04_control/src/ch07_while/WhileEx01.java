



package ch07_while;

/*
 * while 문
 * - while ( 조건식 ) {
 *       실행 코드
 *   }
 * 
 */

public class WhileEx01 {

	public static void main(String[] args) {
		
		int cnt = 1;
		
		while(cnt <= 3) {
			System.out.println(cnt);
			++cnt;
		}
		
	}
	
}















