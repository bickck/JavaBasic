
public class Ex02Print {

	public static void main(String[] args) {
		System.out.println("끈기를 가지고 열심히 하세요...");
		System.out.println("println() 은 출력 후 라인 변경 됩니다.");

		System.out.println();

		System.out.print("print() 는 라인 변경되지 않아");
		System.out.print("확인 되시나요??");
		
		
		// 문자열 : 문자들의 조합. "  "로 작성합니다.
		// 문자열을 더하면 이어지게 됩니다.
		
		System.out.println();
		System.out.println("abc" + "def");

		System.out.println();
		System.out.println(1 + 2 + "data");
		System.out.println("data" + 1 + 2);
		// 문자열이후에 데이터는 문자열로 인식하게 됩니다.

	}
}
